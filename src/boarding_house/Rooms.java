    private void addRoom() {
    try {
        String roomNumber = jTextField1.getText().trim();
        int capacity = (int) jSpinner1.getValue();
        String gender = jComboBox1.getSelectedItem().toString();

        if (roomNumber.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter room number.");
            return;
        }

        if (capacity <= 0) {
            JOptionPane.showMessageDialog(this, "Capacity must be greater than 0.");
            return;
        }

        if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select gender.");
            return;
        }

        java.sql.Connection conn = DBConnection.getConnection();

        String checkSql = "SELECT id FROM rooms WHERE room_number=?";
        PreparedStatement checkPst = conn.prepareStatement(checkSql);
        checkPst.setString(1, roomNumber);
        ResultSet rs = checkPst.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Room number already exists.");
            return;
        }

        String sql = "INSERT INTO rooms (room_number, capacity, gender, occupied, status) "
                + "VALUES (?, ?, ?, 0, 'Available')";

        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, roomNumber);
        pst.setInt(2, capacity);
        pst.setString(3, gender.toLowerCase());

        pst.executeUpdate();

        logHistory("Rooms", "ADD", "Added Room " + roomNumber + " with capacity " + capacity);

        JOptionPane.showMessageDialog(this, "Room added successfully.");

        loadRoomsTable();
        clearRoomFields();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error adding room: " + e.getMessage());
    }
}
    
    
    
    private void updateRoom() {
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a room to update.");
        return;
    }

    try {
        int roomId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
        int occupied = Integer.parseInt(jTable1.getValueAt(selectedRow, 4).toString());

        String roomNumber = jTextField1.getText().trim();
        int capacity = (int) jSpinner1.getValue();
        String gender = jComboBox1.getSelectedItem().toString();

        if (roomNumber.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter room number.");
            return;
        }

        if (capacity <= 0) {
            JOptionPane.showMessageDialog(this, "Capacity must be greater than 0.");
            return;
        }

        if (capacity < occupied) {
            JOptionPane.showMessageDialog(this, "Capacity cannot be lower than occupied tenants.");
            return;
        }

        if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select gender.");
            return;
        }

        String status = occupied >= capacity ? "Not Available" : "Available";

        java.sql.Connection conn = DBConnection.getConnection();

        String checkSql = "SELECT id FROM rooms WHERE room_number=? AND id<>?";
        PreparedStatement checkPst = conn.prepareStatement(checkSql);
        checkPst.setString(1, roomNumber);
        checkPst.setInt(2, roomId);
        ResultSet rs = checkPst.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Another room already uses this room number.");
            return;
        }

        String sql = "UPDATE rooms SET room_number=?, capacity=?, gender=?, status=? WHERE id=?";

        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, roomNumber);
        pst.setInt(2, capacity);
        pst.setString(3, gender.toLowerCase());
        pst.setString(4, status);
        pst.setInt(5, roomId);

        pst.executeUpdate();

        logHistory("Rooms", "UPDATE", "Updated Room " + roomNumber + ". Capacity: " + capacity + ", Status: " + status);

        JOptionPane.showMessageDialog(this, "Room updated successfully.");

        loadRoomsTable();
        clearRoomFields();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error updating room: " + e.getMessage());
    }
}
    
    private void deleteRoom() {
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a room to delete.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this room?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        int roomId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
        String roomNumber = jTable1.getValueAt(selectedRow, 1).toString();
        int occupied = Integer.parseInt(jTable1.getValueAt(selectedRow, 4).toString());

        if (occupied > 0) {
            JOptionPane.showMessageDialog(this, "Cannot delete room. There are still tenants assigned to it.");
            return;
        }

        java.sql.Connection conn = DBConnection.getConnection();

        String sql = "DELETE FROM rooms WHERE id=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, roomId);

        pst.executeUpdate();

        logHistory("Rooms", "DELETE", "Deleted Room " + roomNumber);

        JOptionPane.showMessageDialog(this, "Room deleted successfully.");

        loadRoomsTable();
        clearRoomFields();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error deleting room: " + e.getMessage());
    }
}
    
