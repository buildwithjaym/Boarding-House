    private void logHistory(String module, String action, String details) {
    try {
        java.sql.Connection conn = DBConnection.getConnection();

        String sql = "INSERT INTO history (tenant_id, module, action_type, details) VALUES (NULL, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, module);
        pst.setString(2, action);
        pst.setString(3, details);

        pst.executeUpdate();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "History Log Error: " + e.getMessage());
    }
}
    private void loadRoomsTable() {
    try {
        javax.swing.table.DefaultTableModel model =
                (javax.swing.table.DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);

        java.sql.Connection conn = DBConnection.getConnection();

        String sql = "SELECT * FROM rooms ORDER BY id DESC";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("room_number"),
                rs.getInt("capacity"),
                rs.getString("gender"),
                rs.getInt("occupied"),
                rs.getString("status")
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading rooms: " + e.getMessage());
    }
}
